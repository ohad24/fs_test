from typing import Optional
from fastapi.middleware.cors import CORSMiddleware

from fastapi import FastAPI
import socket




app = FastAPI()

origins = [
    "http://localhost:3000",
    "http://localhost:8000",
]

app.add_middleware(
    CORSMiddleware,
    allow_origins=origins,
    allow_credentials=True,
    allow_methods=["*"],
    allow_headers=["*"],
)


@app.get("/app")
@app.get("/")
def read_root():
    return {"host": socket.gethostname()}


